const os = require('os');
//os module, can create some cool apps with this stuff

//Platform
console.log(os.platform());

//CPU Arch
console.log(os.arch());

//CPU Core Info
console.log(os.cpus());

//Free Memory
console.log(os.freemem());

//Total Memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());

//Uptime
console.log(os.uptime());

